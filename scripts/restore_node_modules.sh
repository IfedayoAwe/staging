#!/bin/bash

tmp_dir=${PROJECT_WORKSPACE}/helpers/tmp
node_modules="${tmp_dir}/node_modules"

# Restore the node_modules directory into the frontend directory.
if [ -d $node_modules ]; then
  echo "Restoring node_modules..."
  mv $node_modules ${PROJECT_WORKSPACE}/frontend/node_modules
else
  echo "node_modules backup not found. Skipping restoration..."
fi

if [ -d $tmp_dir ]; then
  echo "Cleaning up temporary backup directory..."
  rm -r $tmp_dir
fi